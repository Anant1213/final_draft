function switchPage(page) {
    if (page === 'chandigarh') {
        window.location.href = "/chandigarh";
    } else if (page === 'amity') {
        window.location.href = "/amity";
    } else if (page === 'woxsen') {
        window.location.href = "/woxsen";
    } else if (page === 'dbs') {
        window.location.href = "/dbs";
    } else if (page === 'jklu') {
        window.location.href = "/jklu";
    }
}

